def add_title(doc, text, size=16, bold=True, color=None):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text)
    run.font.size = Pt(size)
    run.bold = bold
    if color:
        run.font.color.rgb = color

def add_heading(doc, text, level=1):
    h = doc.add_heading(text, level=level)
    return h

def add_normal(doc, text, bold=False, indent=False):
    p = doc.add_paragraph()
    if indent:
        p.paragraph_format.left_indent = Cm(0.5)
    run = p.add_run(text)
    run.font.size = Pt(11)
    if bold:
        run.bold = True
    return p

def add_poem(doc, title, author, content, notes=""):
    add_normal(doc, f"《{title}》 {author}", bold=True)
    for line in content.split('\n'):
        add_normal(doc, line, indent=True)
    if notes:
        add_normal(doc, f"【注释】{notes}", indent=True)
    doc.add_paragraph()

