def gen_review_doc(unit):
    """生成复习清单Word"""
    doc = Document()
    
    # Title
    add_title(doc, f"三年级下册语文", size=18)
    add_title(doc, unit["title"], size=16, color=RGBColor(0xe6, 0x7e, 0x22))
    doc.add_paragraph()
    
    # Poems
    if unit["poems"]:
        add_heading(doc, "一、必背古诗", level=2)
        for title, author, content, notes in unit["poems"]:
            add_poem(doc, title, author, content, notes)
    
    # Words
    add_heading(doc, f"二、必会字词", level=2)
    table = doc.add_table(rows=1, cols=3)
    table.style = 'Light Grid Accent 1'
    hdr = table.rows[0].cells
    hdr[0].text = "生字"
    hdr[1].text = "拼音"
    hdr[2].text = "组词"
    for w in unit["words"]:
        parts = w.split(" ", 2)
        if len(parts) >= 3:
            row = table.add_row().cells
            row[0].text = parts[0][0]  # char
            row[1].text = parts[0][1:].strip("()")  # pinyin
            row[2].text = parts[2]  # words
    
    # Key sentences
    add_heading(doc, "三、重点句子", level=2)
    for i, s in enumerate(unit["key_sentences"], 1):
        add_normal(doc, f"{i}. {s}")
    
    # Tips
    add_heading(doc, "四、复习要点", level=2)
    for t in unit["tips"]:
        add_normal(doc, f"• {t}")
    
    return doc

